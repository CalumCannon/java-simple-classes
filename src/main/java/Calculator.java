public class Calculator {

    public Calculator(){
    }

    public int addNums(int val1,int val2){
      return  val1 + val2;
    }
    public int subtract(int val1,int val2){
        return  val1 - val2;
    }
    public int multiply(int val1,int val2){
        return  val1 * val2;
    }
    public double divide(int val1,int val2){
        return  val1 / val2;
    }

}
